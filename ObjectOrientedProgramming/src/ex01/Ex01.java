package ex01;
public class Ex01 {
    public static void display(int[] array) {
	for (int i = 0; i < array.length; i++) {
	      System.out.print(array[i] + " ");
	}
	System.out.println();
    }

    public static void display(String[] array) {
          for (int i = 0; i < array.length; i++) {
	      System.out.print(array[i] + " ");
          }
	System.out.println();
    }
}
